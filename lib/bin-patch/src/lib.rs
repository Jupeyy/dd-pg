use std::{
    io::{Read, Write},
    ops::BitXor,
};

use zstd::dict::{DecoderDictionary, EncoderDictionary};

pub fn diff(old: &[u8], new: &[u8], patch: &mut Vec<u8>) -> anyhow::Result<()> {
    let dict = EncoderDictionary::new(old, 0);
    let mut enc = zstd::Encoder::with_prepared_dictionary(patch, &dict)?;
    enc.write_all(new)?;
    enc.finish()?;
    Ok(())
}

pub fn patch(old: &[u8], patch: &[u8], new: &mut Vec<u8>) -> anyhow::Result<()> {
    let dict = DecoderDictionary::new(old);
    let mut dec = zstd::Decoder::with_prepared_dictionary(std::io::Cursor::new(patch), &dict)?;
    dec.read_to_end(new)?;
    dec.finish();
    Ok(())
}

/// A more optimized alternative to [`diff`] if `old` and `new` are the same size binary wise,
/// e.g. if it is not dynamically sized.
pub fn diff_exact_size(old: &[u8], new: &[u8], patch: &mut Vec<u8>) -> anyhow::Result<()> {
    anyhow::ensure!(old.len() == new.len(), "length of old & new must be equal");
    patch.reserve(old.len());
    for (index, old) in old.iter().enumerate() {
        patch.push(old.bitxor(new[index]));
    }
    Ok(())
}

/// A more optimized alternative to [`patch`] if `old` and `new` are the same size binary wise,
/// e.g. if it is not dynamically sized.
/// `patch` must have been created by [`diff_exact_size`].
pub fn patch_exact_size(old: &[u8], patch: &[u8], new: &mut Vec<u8>) -> anyhow::Result<()> {
    anyhow::ensure!(
        old.len() == patch.len(),
        "length of old & patch must be equal"
    );
    new.reserve(old.len());
    for (index, old) in old.iter().enumerate() {
        new.push(old.bitxor(patch[index]));
    }
    Ok(())
}

