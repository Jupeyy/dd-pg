    pub fn compress<W: std::io::Write>(
        uncompressed_file: &[u8],
        writer: &mut W,
    ) -> anyhow::Result<()> {
        let mut write_data: Vec<u8> = Default::default();
        brotli::CompressorWriter::new(&mut write_data, 4096, 8, 22).write_all(uncompressed_file)?;
        writer.write_all(&(write_data.len() as u32).to_le_bytes())?;
        writer.write_all(&write_data)?;
        Ok(())
    }

    fn snapshot_delta_bsdiff(
        snap1: &[u8],
        snap2: &[u8],
        writer: &mut Vec<u8>,
        compressed_writer: &mut Vec<u8>,
    ) {
        writer.clear();
        compressed_writer.clear();
        let benchmark = Benchmark::new(DO_BENCH);
        bsdiff::diff(&snap1, &snap2, writer).unwrap();
        benchmark.bench(&format!("bsdiff: {}", writer.len()));
        if DO_COMP {
            compress(&writer, compressed_writer).unwrap();
            benchmark.bench(&format!("bsdiff compressed: {}", compressed_writer.len()));
        }
        compressed_writer.clear();
        bsdiff::patch(
            &snap1,
            &mut std::io::Cursor::new(&mut *writer),
            compressed_writer,
        )
        .unwrap();
        benchmark.bench(&format!("bsdiff patch: {}", compressed_writer.len()));
    }

